def ceaser(text, key):
    text = text.lower()
    key = int(key)
    letterShift = letters[key:] + letters[:key]
    crypt = str()
    for i in text:
        crypt += letterShift[letters.index(i)]
    return crypt

def vernam(text, key):
    crypt = str()
    p = 0
    for char in text:
        crypt += chr(ord(char) ^ ord(key[p]))
        p += 1
        if p==len(key):
            p = 0
    return crypt
